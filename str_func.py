def text_upper_case(text):
    """
    function takes a string as input and returns it with all capital letters
    :param text: any string
    :return: text with capital letters
    """
    return text.upper()


def text_title_case(text):
    """
    function capitalizes the first letters of each word in the input string
    :param text: any string
    :return: text with capitalized first letters
    """
    return text.title()
